
public class varicontainers {
    public static void main(String[] args) {
        int a;
        a=500;
        System.out.println(a);
        a=a+5;
        System.out.println(a);
        a=a-3;
        System.out.println(a);
        a=a*5;
        System.out.println(a);
        a=a/3;
        System.out.println(a);
        a=a%2;
        System.out.println(a);
        
    }
    
}
